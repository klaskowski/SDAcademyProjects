public class PostAreaCode{
    String city;
    String streetName;
    Integer minHouseNo;
    Integer maxHouseNo;

    public PostAreaCode(String city, String streetName, Integer minHouseNo, Integer maxHouseNo){
        this.city = city;
        this.streetName = streetName;
        this.minHouseNo = minHouseNo;
        this.maxHouseNo = maxHouseNo;
    }

}
